#Displacement formula

final_velocity = int(input("Enter value of the final velocity [v]: "))
initial_velocity = int(input("Enter value of the initial velocity [v]: "))
acceleration = int(input("Enter the value of acceleration: "))

displacement = ((final_velocity**2)-(initial_velocity**2))/(2*acceleration)

print(f"The displacement is {displacement} m")